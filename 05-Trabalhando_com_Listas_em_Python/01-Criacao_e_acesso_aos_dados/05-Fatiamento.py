lista = ["p", "y", "t", "h", "o", "n"]

print(lista[2:])      # A partir da 2ª letra    y, t, h, o, n
print(lista[:2])      # Até a 2ª letra    p, y   
print(lista[1:3])     # A partir a 2ª letra até a 3ª
print(lista[0:3:2])   # até a terceira letra pulando 2
print(lista[::])      # Todas as letras
print(lista[::-1])    # O inverso da letras