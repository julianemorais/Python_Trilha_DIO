#Definir o valor inicial ou subescrever o valor de uma variável
#Atribuição simples
saldo = 200

print(saldo)

#Atribuição com adição
saldo += 10
print(saldo)

#Atribuição com subtração
saldo -= 5
print(saldo)

#Atribuição com multiplicação
saldo *= 10
print(saldo)

#Atribuição com divisão
saldo /= 2
print(saldo)

#Atribuição com exponenciação
saldo **= 2
print(saldo)
