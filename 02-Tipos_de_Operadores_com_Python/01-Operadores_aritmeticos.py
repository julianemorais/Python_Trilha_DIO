produto1 = 20
produto2 = 10

#Adição
print(produto1 + produto2)

#Subtração
print(produto1 - produto2)

#Multiplicação
print(produto1 * produto2)

#Divisão
print(produto1 / produto2)

#Divisão inteira
print(produto1 // produto2)

#Módulo (Resto da divisão)
print(produto1 % produto2)

#exponenciação
print(produto1 ** produto2)


