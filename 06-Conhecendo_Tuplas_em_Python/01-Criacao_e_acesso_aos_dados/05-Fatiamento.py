tupla = ["p", "y", "t", "h", "o", "n"]

print(tupla[2:])      # A partir da 2ª letra    y, t, h, o, n
print(tupla[:2])      # Até a 2ª letra    p, y   
print(tupla[1:3])     # A partir a 2ª letra até a 3ª
print(tupla[0:3:2])   # até a terceira letra pulando 2
print(tupla[::])      # Todas as letras
print(tupla[::-1])    # O inverso da letras